package com.example.bryantyrrell.vdiapp.GPSMap.Video;

public class VideoCutTimes {

    private long TimeBefore;
    private long TimeAfter;
    private long IncidentTime;
    private int Index;


    public long getTimeBefore() {
        return TimeBefore;
    }

    public void setTimeBefore(long timeBefore) {
        TimeBefore = timeBefore;
    }

    public long getTimeAfter() {
        return TimeAfter;
    }

    public void setTimeAfter(long timeAfter) {
        TimeAfter = timeAfter;
    }

    public long getIncidentTime() {
        return IncidentTime;
    }

    public void setIncidentTime(long incidentTime) {
        IncidentTime = incidentTime;
    }

    public int getIndex() {
        return Index;
    }

    public void setIndex(int index) {
        Index = index;
    }



}
